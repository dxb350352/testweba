public class SingleDouble {
    public static void main(String[] args) {
        Float f = 0.0000123456789f;
        System.out.println(f.toString());
        f = 0.000123456789f;
        System.out.println(f.toString());
        f = 0.00123456789f;
        System.out.println(f.toString());
        f = 0.0123456789f;
        System.out.println(f.toString());
        f = 0.123456789f;
        System.out.println(f.toString());
        f = 1.23456789f;
        System.out.println(f.toString());
        f = 12.3456789f;
        System.out.println(f.toString());
        f = 123.456789f;
        System.out.println(f.toString());
        f = 1234.56789f;
        System.out.println(f.toString());
        f = 12345.6789f;
        System.out.println(f.toString());
        f = 123456.789f;
        System.out.println(f.toString());
        f = 1234567.89f;
        System.out.println(f.toString());
        f = 12345678.9f;
        System.out.println(f.toString());
        f = 123456789f;
        System.out.println(f.toString());
    }
}
